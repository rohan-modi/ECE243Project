void updateCurrentFrame(struct Player *player){
    int touched_ground = 0;
    if(player->yVelocity != 0) {
        player->currentFrame = player->frameCount - 1; // Assuming the last frame is for jumping
    } else {
        // Check if any movement key is pressed
        if(*(player->downControl) || *(player->leftControl) || *(player->rightControl)) {
            player->currentFrame = (player->currentFrame + 1) % (player->frameCount - 1); // Cycle through frames except the last one
            touched_ground = 0;
        } else {
            // The player has touched the ground but no movement key is pressed
            touched_ground = 1;
        }

        if(touched_ground == 1) {
            player->currentFrame = 0; // Set to the first frame, typically standing/idle position
        }
    }
}

